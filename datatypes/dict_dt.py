# Dictionary Datatype
# Collection of key:value pairs
# Mutable datatype
# Keys cannot be duplicated
# Values can be duplicated

d={1:100,2:200j,3:False}
print(type(d))
print(d)

d1={1:100,1:200j,1:False}
print(type(d1))
print(d1)

d2={1:100,2:100,3:100}
print(type(d2))
print(d2)

